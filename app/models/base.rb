#----------------------------------------------------------------------
# Base - 汎用的な親クラス
#----------------------------------------------------------------------
class Base

  attr_reader :params

  # [] - インデクサでparamsにアクセスさせる
  #--------------------------------------------------------------------
  def [](index)
    @params[index]
  end

  # exist? - レコードの存在を確認
  #--------------------------------------------------------------------
  def exist?
    ! @params.nil?
  end

end
